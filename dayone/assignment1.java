/*Assignment: Data Type and Literal Explorer
Objective: Write a Java program that demonstrates the use of primitive data types and literals in simple ways.*/
package dayone;
public class assignment1 {
    public static void main(String[] args) {
        // Declare and initialize variables for each of the following primitive data types: byte, short, int, long, float, double, boolean, char.
        byte bytevalue = 24;
        short shortvalue = 123;
        int intvalue = -23;
        long longvalue = 464327;
        float floatvalue = 6.5f;
        double doublevalue = 6.5;
        boolean booleanvalue = true;
        char charvalue = 'A';
    System.out.println("this is a byte\t" + bytevalue);
    System.out.println("this is a short\t" + shortvalue);
    System.out.println("this is a int\t" + intvalue);
    System.out.println("this is a long\t" + longvalue);
    System.out.println("this is a float\t" + floatvalue);
    System.out.println("this is a double\t" + doublevalue);
    System.out.println("this is a boolean\t" + booleanvalue);
    System.out.println("this is a char\t" + charvalue);
    } 
}